#include <unistd.h>

int		main(int argc, char **argv)
{
	int a;
	int b;

	a = 1;
	b = 0;
	while (a < argc)
	{
		while (argv[a][b] != '\0')
		{
			write(1, &argv[a][b], 1);
			b++;
		}
		write(1, "\n", 1);
		b = 0;
		a++;
	}
	return (0);
}
