int		ft_str_is_numeric(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] > 47 && str[a] < 58)
		{
			a++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
