int		ft_atoi(char *str)
{
	int a;
	int b;
	int	c;

	a = 0;
	b = 0;
	c = 0;
	while ((str[a] == '\n') || (str[a] == '\t') || (str[a] == '\v') ||
	(str[a] == '\f') || (str[a] == '\r') || (str[a] == ' '))
		a++;
	while ((str[a] == '-') || (str[a] == '+'))
	{
		if (str[a] == '-')
			b++;
		a++;
	}
	while ((str[a] >= '0') && (str[a] <= '9'))
	{
		c = c * 10;
		c = c + (str[a] - '0');
		a++;
	}
	if (b % 2 != 0)
		c *= (-1);
	return (c);
}
